q3BoxDef CubeModel::GetBoxDef(){
	q3BoxDef def;

	q3Transform tx;
	q3Identity(tx);

	// Set the extents of the box
	def.Set(tx, g2q( GetScaling() ));

	def.SetDensity(mDensity);
	def.SetFriction(mFriction);
	def.SetRestitution(mRestitution);

	return def;
}

